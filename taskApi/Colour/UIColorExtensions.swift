import Foundation
import UIKit

extension UIColor {
    class var additionalGray: UIColor {
        return UIColor(red: 95.0 / 255.0, green: 93.0 / 255.0, blue: 112.0 / 255.0, alpha: 1.0)
    }

    class var additionalGrayTransparent: UIColor {
        return UIColor(red: 95.0 / 255.0, green: 93.0 / 255.0, blue: 112.0 / 255.0, alpha: 0.4)
    }

}
